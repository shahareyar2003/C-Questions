#include<iostream>
using namespace std;
int main(){
    int a;
    cout<<"Assignment Operator in C++"<<endl;
    cout<<"\nThe value of a= "<<(a=5);
    cout<<"\nThe value of a+=1 "<<(a+=1);
    cout<<"\nThe value of a-=1 "<<(a-=1);
    cout<<"\nThe value of a*=1 "<<(a*=1);
    cout<<"\nThe value of a/=1 "<<(a/=1);
    cout<<"\nThe value of a%=1 "<<(a%=1);
}