#include<iostream>
using namespace std;
int main(){
    int a=10,b=5;
    cout<<"Arithmetic Operator in C++"<<endl;
    cout<<"\nThe value of a+b "<<(a+b);
    cout<<"\nThe value of a-b "<<(a-b);
    cout<<"\nThe value of a*b "<<(a*b);
    cout<<"\nThe value of a/b "<<(a/b);
    cout<<"\nThe value of a%b "<<(a%b);
    cout<<"\nThe value of a++ "<<(a++);
    cout<<"\nThe value of a-- "<<(a--);
    cout<<"\nThe value of ++a "<<(++a);
    cout<<"\nThe value of --a "<<(--a);

}