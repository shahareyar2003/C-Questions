// round() Method
//     Rounds gave numbers to the closest integer. It is defined in the header files: <cmath> and <ctgmath>.
#include<iostream>
#include<cmath>
using namespace std;
int main(){
     double x = 1.411, y = 1.500, z = 1.611;
 
    cout << round(x) << endl;
    cout << round(y) << endl;
    cout << round(z) << endl;
    return 0;
}